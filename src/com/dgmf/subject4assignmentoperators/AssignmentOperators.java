package com.dgmf.subject4assignmentoperators;

public class AssignmentOperators {
    public static void main(String[] args) {
        /* ****** Assignment Operators ****** */
        System.out.println("****** Assignment Operators ******");

        /* ****** = (Simple Assignment) ****** */
        System.out.println("****** = (Simple Assignment) ******");
        int num1 = 12;
        System.out.println("Num1 = " + num1);

        /* ****** += (Add and Assignment) ****** */
        System.out.println("****** += (Add and Assignment) ******");
        num1 += 12; // num1 = num1 + 12
        System.out.println("Num1 = " + num1);

        /* ****** -= (Subtract and Assignment) ****** */
        System.out.println("****** -= (Subtract and Assignment) ******");
        num1 -= 12; // num1 = num1 - 12
        System.out.println("Num1 = " + num1);

        /* ****** *= (Multiply and Assignment) ****** */
        System.out.println("****** *= (Multiply and Assignment) ******");
        num1 *= 12; // num1 = num1 * 12
        System.out.println("Num1 = " + num1);

        /* ****** /= (Divide and Assignment) ****** */
        System.out.println("****** /= (Divide and Assignment) ******");
        num1 /= 12; // num1 = num1 / 12
        System.out.println("Num1 = " + num1);

        /* ****** %= (Divide and Assign the Rest) ****** */
        System.out.println("****** %= (Divide and Assign the Rest) ******");
        num1 %= 12; // num1 = num1 % 12
        System.out.println("Num1 = " + num1);

    }
}
